package ref;

public class SleepParameter {
	private String grade;
	private String name;

	public SleepParameter(String grade, String name) {
		this.grade = grade;
		this.name = name;
	}

	public String getGrade() {
		return grade;
	}

	public void setGrade(String grade) {
		this.grade = grade;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}